# declaração de lista
nomes = ['Fulano','Cicrano','Beltrano']

# delimitador
separador = '-'

# string que irá juntar os nomes
nomes_string = separador.join(nomes)

# exibe o valor na tela
print(nomes_string)

# NOTE: esse algoritmo é últil caso deseje juntar valores diferentes em uma única variável