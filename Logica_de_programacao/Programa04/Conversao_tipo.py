# declaração de variável
valor = 15

# exibe na tela o valor e o tipo de variável
print(valor, type(valor))

# converte para string
valor = str(valor)

# exibe na tela o valor e o novo tipo
print(valor, type(valor))

# converte para int
valor = int(valor)

# exibe na tela o valor e o novo tipo
print(valor, type(valor))

# converte para float
valor = float(valor)

# exibe na tela o valor e o novo tipo
print(valor, type(valor))