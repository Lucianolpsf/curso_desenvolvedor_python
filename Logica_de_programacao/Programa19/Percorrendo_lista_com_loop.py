# declaração de variáveis
lista_nomes = ['Fualno', 'Cicrano', 'Beltrano', 'Joana', 'Maria']

# imprime um item da lista usando laço de repetição
for nome in lista_nomes:
    print(nome)

# NOTE: o 'for' pode ser usado tanto como o 'for' tradicional como para substituir o 'foreach' das outras linguagens.