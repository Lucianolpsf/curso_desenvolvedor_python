# NOTE: é possível ordenar a lista em ordem alfabética, no caso de strings, como será visto a seguir.

# declaração da lista
estados = ['Distrito Federal', 'Maranhão', 'Acre', 'Goiás', 'Minas Gerais']

# exibe a lista original na tela
print(estados)

# ordena a lista em ordem alfabética
estados.sort()

# reexibe a lista ordenada
print(estados)